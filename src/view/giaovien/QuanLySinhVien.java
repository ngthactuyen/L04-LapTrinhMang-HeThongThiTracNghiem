    private void btn_themmoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themmoiActionPerformed
        // TODO add your handling code here:
        String maSV = txt_masv.getText();
        String hoten = txt_hoten.getText();
        String lop = txt_lop.getText();
        String matkhau = txt_matkhau.getText();
        
        if (maSV.equals("") || hoten.equals("") || lop.equals("") || matkhau.equals("")) {
            lb_thongbao.setText("Tất cả các trường không được bỏ trống");
        }else{
            SinhVien sv = new SinhVien(lop, maSV, matkhau, hoten);
            try {
                int i = sinhvienCtr.dangky(sv);
                if (i == 0) {
                    lb_thongbao.setText("Mã sinh viên đã có người sử dụng, hãy nhập lại");
                }
                if (i == 1) {
                    JOptionPane.showMessageDialog(rootPane, "Thêm mới thành công");
                    loadData();
                }
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(QuanLySinhVien.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btn_themmoiActionPerformed

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
        String maSV = txt_masv.getText();
        if (maSV.equals("")) {
            lb_thongbao.setText("Bạn cần chọn một sinh viên để xóa");
        } else {
            int click = JOptionPane.showConfirmDialog(rootPane, "Bạn chắc chắn muốn xóa?");
            if (click == JOptionPane.YES_OPTION) {
                try {
                    int i = sinhvienCtr.xoa(maSV);
                    if (i == 0) {
                        lb_thongbao.setText("Không có mã sinh viên như trên, hãy nhập lại");
                    }
                    if (i == 1) {
                        JOptionPane.showMessageDialog(rootPane, "Xóa thành công");
                        loadData();
                    }
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(QuanLySinhVien.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(QuanLySinhVien.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btn_xoaActionPerformed

