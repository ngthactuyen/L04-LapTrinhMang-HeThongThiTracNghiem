    public void loadData() throws ClassNotFoundException, SQLException{
        txt_masv.setText("");
        txt_hoten.setText("");
        txt_lop.setText("");
        txt_matkhau.setText("");
        lb_thongbao.setText("");
        
        modelSinhVien = new DefaultTableModel();
        modelSinhVien.addColumn("Mã sinh viên");
        modelSinhVien.addColumn("Họ tên");
        modelSinhVien.addColumn("Lớp");
        modelSinhVien.addColumn("Mật khẩu");
        //Load data vào bảng
        dsSinhVien = sinhvienCtr.danhsachSinhVien();
        for (int i = 0; i < dsSinhVien.size(); i++) {
            SinhVien sv = dsSinhVien.get(i);
            Object[] rowData = {
                sv.getId(), sv.getHoten(), sv.getLop(), sv.getMatkhau()
            };
            modelSinhVien.addRow(rowData);
        }
        tbl_data.setModel(modelSinhVien);
    }
    
